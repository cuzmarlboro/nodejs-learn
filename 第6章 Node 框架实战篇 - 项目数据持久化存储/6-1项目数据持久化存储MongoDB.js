// 1. MongoDB 是什么？
// MongoDB 是一个基于分布式文件存储的NoSQL数据库，介于关系数据库和非关系数据库之间，是非关系数据库中功能最丰富、最像关系数据库的。
// 数据结构由键值对组成，数据格式灵活，可以存储复杂的数据类型，支持动态查询。

// 2. MongoDB 有哪些特性？
// 文档型数据库，拓展性强，拥有强大的查询语言，多种存储引擎，高性能，高可用，支持数据嵌入，子文档查询，支持副本集与分片

// 3. MongoDB 的适用场景？
// 对数据结构要求不严格，数据一致性要求不高的场景
// 需要灵活的数据模型，需要处理大量数据，需要高性能，高可用，易扩展的场景
// 需要处理大量非结构化数据，需要处理大量半结构化数据，需要处理大量结构化数据